/** ISO-2 koddan Unicode regional-indicator bayrak emoji üretir. */
export function getFlagEmoji(code: string): string {
  const upper = code.toUpperCase();
  if (upper.length !== 2) return "";
  const A = 127397;
  return String.fromCodePoint(A + upper.charCodeAt(0), A + upper.charCodeAt(1));
}

/** Koddan ülke kaydı bul. */
export function getCountryByCode(code: string): Country | undefined {
  const upper = code.toUpperCase();
  return ALL_COUNTRIES.find((c) => c.code === upper);
}

/**
 * Verilen değeri ülke kaydına çözer. Kabul edilen formatlar:
 * - ISO-2 kod (örn. "TR", "us")
 * - İngilizce ad (örn. "Turkey", "United States")
 * - Türkçe ad (örn. "Türkiye", "Amerika Birleşik Devletleri")
 *
 * Hiçbiri eşleşmezse Türkiye'ye düşer.
 */
export function resolveCountry(value: string | undefined | null): Country {
  const tr = ALL_COUNTRIES.find((c) => c.code === "TR")!;
  if (!value) return tr;
  const trimmed = value.trim();
  if (!trimmed) return tr;

  if (trimmed.length === 2) {
    const byCode = getCountryByCode(trimmed);
    if (byCode) return byCode;
  }

  const lower = trimmed.toLocaleLowerCase("tr");
  const byName = ALL_COUNTRIES.find(
    (c) =>
      c.nameEN.toLowerCase() === trimmed.toLowerCase() || c.nameTR.toLocaleLowerCase("tr") === lower
  );
  return byName || tr;
}
